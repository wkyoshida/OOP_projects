    private void dropButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropButtonActionPerformed
        // TODO add your handling code here:
        List< Bookings > customersDropped = bookingQueries.droppedCustomers(dropFlightComboBox.getSelectedItem().toString());
        List< String > allFlights = flightQueries.getAllFlights();
        
        for (Bookings b: customersDropped){
            
            String rebookCustomer = b.getCustomer();
            Date rebookDate = b.getDay();
            int rebooked = 0;
            
            for (String f: allFlights){
                
                if (rebooked == 0){
                    int available = flightQueries.getFlightSeats(f);
                    int taken = bookingQueries.seatsBooked(f, rebookDate);

                    if (taken < available) {
                        int resultRebooking = bookingQueries.addBooking(rebookCustomer, f, rebookDate);
                        if (resultRebooking == 1){
                            String message = "Customer, " + rebookCustomer +  ", rebooked on flight " + f + " on " + rebookDate.toString();
                            JOptionPane.showMessageDialog(this, message, "Customer rebooked", JOptionPane.PLAIN_MESSAGE);
                            rebooked = 1;
                        }
                        else {
                            String errorMessage = "Customer, " + rebookCustomer +  ", could not be rebooked!";
                            JOptionPane.showMessageDialog(this, errorMessage, "Error", JOptionPane.PLAIN_MESSAGE);
                        } 
                    }
                }
            }
            if (rebooked == 0) {
                String failMessage = "Customer, " + rebookCustomer +  ", could not be rebooked!";
                JOptionPane.showMessageDialog(this, failMessage, "Error", JOptionPane.PLAIN_MESSAGE);
            }
        }
         
        int resultDeleteWait = waitingQueries.dropWaitFlight(dropFlightComboBox.getSelectedItem().toString());
        if (resultDeleteWait != 0){
            String newMessage = "Waitlists for flight " + dropFlightComboBox.getSelectedItem().toString() +  " were deleted.";
            JOptionPane.showMessageDialog(this, newMessage, "Waitlists deleted", JOptionPane.PLAIN_MESSAGE);
        }
        int resultDeleteFlight = flightQueries.dropFlight(dropFlightComboBox.getSelectedItem().toString());
        flightComboBox.setModel(new javax.swing.DefaultComboBoxModel(flightQueries.getAllFlights().toArray()));
        bookingFlightComboBox.setModel(new javax.swing.DefaultComboBoxModel(flightQueries.getAllFlights().toArray()));
        dropFlightComboBox.setModel(new javax.swing.DefaultComboBoxModel(flightQueries.getAllFlights().toArray()));
    }//GEN-LAST:event_dropButtonActionPerformed

