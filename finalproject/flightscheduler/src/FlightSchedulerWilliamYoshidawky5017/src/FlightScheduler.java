    private void bookingButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookingButtonActionPerformed
        // TODO add your handling code here:
        int available = flightQueries.getFlightSeats(bookingFlightComboBox.getSelectedItem().toString());
        int taken = bookingQueries.seatsBooked(bookingFlightComboBox.getSelectedItem().toString(), Date.valueOf(bookingDateComboBox.getSelectedItem().toString()));
        int result = 0;
        
        if (taken < available) {
            result = bookingQueries.addBooking(bookingCustomerComboBox.getSelectedItem().toString(),bookingFlightComboBox.getSelectedItem().toString(),Date.valueOf(bookingDateComboBox.getSelectedItem().toString()));
            if (result == 1){
                JOptionPane.showMessageDialog(this, "Customer booked!", "Customer booked", JOptionPane.PLAIN_MESSAGE);
            }
            else {
                JOptionPane.showMessageDialog(this, "Customer not booked!", "Error", JOptionPane.PLAIN_MESSAGE);
            }
        }
        else {
            result = waitingQueries.addWaitList(bookingCustomerComboBox.getSelectedItem().toString(),bookingFlightComboBox.getSelectedItem().toString(),Date.valueOf(bookingDateComboBox.getSelectedItem().toString()));
            if (result == 1){
                JOptionPane.showMessageDialog(this, "Flight full! Customer waitlisted!", "Customer waitlisted", JOptionPane.PLAIN_MESSAGE);
            }
            else {
                JOptionPane.showMessageDialog(this, "Flight full! Customer not waitlisted", "Error", JOptionPane.PLAIN_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_bookingButtonActionPerformed
    
